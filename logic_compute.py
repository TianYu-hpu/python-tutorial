#!/usr/bin/env python3
# -*- coding: utf-8 -*-

#逻辑运算
t = True
f = False

#与运算
print("t and f", t and f)
#或
print("t or f", t or f)
#非
print("not f", not f)
print("not t", not t)
#抑或运算
print("t != f", t != f)
print("None == 1", None == 1)
print("None == None", None == None)
print("None == False", None == False)
print("type(None)", type(None))
